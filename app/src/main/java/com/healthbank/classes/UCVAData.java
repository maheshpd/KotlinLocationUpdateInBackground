package com.healthbank.classes;

public class UCVAData {
    String D="";
    String N="";
    String PH="";
    String VN="";
    String Add="";
    String NV="";

    public UCVAData(){

    }

    public String getD() {
        return D;
    }

    public void setD(String d) {
        D = d;
    }

    public String getN() {
        return N;
    }

    public void setN(String n) {
        N = n;
    }

    public String getPH() {
        return PH;
    }

    public void setPH(String PH) {
        this.PH = PH;
    }

    public String getVN() {
        return VN;
    }

    public void setVN(String VN) {
        this.VN = VN;
    }

    public String getAdd() {
        return Add;
    }

    public void setAdd(String add) {
        Add = add;
    }

    public String getNV() {
        return NV;
    }

    public void setNV(String NV) {
        this.NV = NV;
    }
}
